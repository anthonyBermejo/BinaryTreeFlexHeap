package position;

/**
 * Interface representing the Position ADT containing only one method that
 * returns the element it contains
 * 
 * @author Anthony-Virgil Bermejo (6982166)
 *
 */
public interface Position {
	/**
	 * Sole method in interface that returns the element a Position object contains
	 * @return value of Position object
	 */
	char element();
}
